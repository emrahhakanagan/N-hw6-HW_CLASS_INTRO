public class Author {
    public String name;
    public String patronymic;
    public String surname;
    public int rating;

    public  Author() {

    }

    public Author(String name, String patronymic, String surname, int rating) {
        this.name = name;
        this.patronymic = patronymic;
        this.surname = surname;
        this.rating = rating;
    }
}
